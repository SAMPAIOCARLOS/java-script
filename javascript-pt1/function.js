function soma(valor1, valor2){
    const resultado = valor1 + valor2
    return resultado
}

function multiplicar(valor1, valor2){
    const resultado =valor1 * valor2
    return resultado
}

function subitracao(valor1, valor2){
    const resultado = valor1 - valor2
    return resultado
}

function divisao(valor1, valor2){
    const resultado = valor1 / valor2
    return resultado
}

